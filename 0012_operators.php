/*
Операторы массива

Оператор	    Имя	                Пример	        Результат
+	            Союз	            $x + $y	        Объединение $ x и $ y	
==	            Равенство	        $x == $y	    Возвращает true, если $ x и $ y имеют одинаковые пары ключ / значение	
===	            Идентичность	    $x === $y	    Возвращает true, если $x и $y содержат одинаковые пары «ключ-значение» в том же порядке и того же типа	
!=	            Неравенство	        $x != $y	    Возвращает true, если $ x не равно $ y	
<>	            Неравенство	        $x <> $y	    Возвращает true, если $ x не равно $ y	
!==	            Неидентичность	    $x !== $y	    Возвращает true, если $ x не совпадает с $ y

*/


/*
Операторы условного присваивания

Оператор	Имя	                Пример	                                Результат	
?:	        Троичный	        $x = expr1 ? expr2 : expr3	            Возвращает значение $x.
                                                                        Значение $x равно expr2, если expr1 = ИСТИНА.
                                                                        Значение $x равно expr3, если expr1 = ЛОЖЬ	
??	        Нулевое слияние	    $ x = выражение 1 ?? выражение 2	    Возвращает значение $x.
                                                                        Значение $x равно expr1, если expr1 существует и не равно NULL.
                                                                        Если expr1 не существует или равно NULL, значение $x равно expr2.
                                                                        Введено в PHP 7

