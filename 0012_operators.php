
Арифметические операторы

Оператор	Имя	                    Пример	    Результат
+	        Дополнение	            $x + $y	    Сумма $ x и $ y	
-	        Вычитание	            $x - $y	    Разница в $ x и $ y	
*	        Умножение	            $x * $y	    Произведение $ x и $ y	
/	        Разделение	            $x / $y	    Частное от $ x и $ y	
%	        Модуль упругости	    $x % $y	    Остаток от $ x, деленный на $ y	
**	        Возведение в степень	$x ** $y	Результат возведения $ x в $ y -ю степень

*/

/*
Операторы присваивания 

Назначение	    То же самое, что и...	    Описание
x = y	        x = y       	            Левому операнду присваивается значение выражения справа	
x += y  	    x = x + y   	            Дополнение	
x -= y	        x = x - y   	            Вычитание	
x *= y  	    x = x * y   	            Умножение	
x /= y  	    x = x / y   	            Разделение	
x %= y  	    x = x % y   	            Модуль упругости

*/

/*
Операторы сравнения

Оператор	Имя	                    Пример	        Результат
==	        Равный	                $x == $y	    Возвращает true, если $ x равно $ y	
===	        Идентичный	            $x === $y	    Возвращает true, если $x равен $y и они одного типа	
!=	        Не равны	            $x != $y	    Возвращает true, если $ x не равно $ y	
<>	        Не равны	            $x <> $y	    Возвращает true, если $ x не равно $ y	
!==	        Не идентичные	        $x !== $y	    Возвращает true, если $x не равно $y или они не относятся к одному типу	
>	        Больше , чем	        $x > $y	        Возвращает true, если $ x больше, чем $ y	
<	        Меньше , чем	        $x < $y	        Возвращает true, если $ x меньше, чем $ y	
>=	        Больше или равно	    $x >= $y	    Возвращает true, если значение $ x больше или равно значению $ y	
<=	        Меньше или равно	    $x <= $y	    Возвращает true, если $ x меньше или равно $ y	
<=>	        Космический корабль	    $x <=> $y	    Возвращает целое число, меньшее, равное или большее нуля, в зависимости от того, меньше ли $x, равно ли $x или больше $y. Представлено в PHP 7.

*/


/*
Операторы инкремента / Декремента

Оператор	То же самое, что и...	        Описание
++$x	    Предварительное увеличение	    Увеличивает $ x на единицу, затем возвращает $ x	
$x++	    Последующий инкремент	        Возвращает $ x, затем увеличивает $ x на единицу	
--$x	    Предварительное уменьшение	    Уменьшает значение $ x на единицу, затем возвращает значение $x	
$x--	    Пост-декремент	                Возвращает $ x, затем уменьшает $ x на единицу

*/


/*
Логические операторы

Оператор	Имя	    Пример	        Результат
and	        И	    $x и $ y	    Истинно, если оба значения $ x и $ y истинны
or	        Или	    $x или $ y	    True , если значение $ x или $ y равно true
xor	        Xor	    $x xor $y	    Истинно, если либо $ x, либо $ y истинны, но не оба сразу
&&	        И	    $x && $y	    Истинно, если оба значения $ x и $ y истинны
||	        Или	    $x || $y	    True , если значение $ x или $ y равно true
!	        Нет	    !$x	            True , если значение $x не равно true

*/


