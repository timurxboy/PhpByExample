Некоторые предопределённые переменные в PHP являются «суперглобальными», что означает, 
что они всегда доступны, независимо от области видимости, и вы можете получить к ним доступ из любой функции, 
класса или файла без каких-либо дополнительных действий.

Суперглобальными переменными PHP являются:
    $GLOBALS
    $_SERVER
    $_REQUEST
    $_POST
    $_GET
    $_FILES
    $_ENV
    $_COOKIE
    $_SESSION



